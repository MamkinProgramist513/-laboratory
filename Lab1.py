from math import *

x = float(input("Введите значение x -> "))
y = float(input("Введите значение y -> "))
z = float(input("Введите значение z -> "))

# Проверки на ошибки
if x == 0:
    print("Ошибка: x не должен быть равен нулю (деление на ноль).")
elif z == 0:
    print("Ошибка: z не должен быть равен нулю (деление на ноль).")
elif y / x < 0:
    print("Ошибка: y/x должно быть неотрицательным (нельзя извлечь кубический корень из отрицательного числа).")
elif x < 0 and y / z != int(y / z):
    print("Ошибка: при отрицательном x и дробной степени возникает ошибка (нужна целая степень).")
else:
    # Первая часть выражения: |x^(y/z) - (y/x)^(1/3)|
    a1 = fabs(pow(x, y / z) - pow(y / x, 1 / 3))

    # Вторая часть выражения: (y - x) * (cos(y) - z^2) / (1 + (y - x)^2)
    a2 = (y - x) * (cos(y) - z**2) / (1 + (y - x)**2)

    # Итоговое выражение
    s = a1 + a2

    print("Результат: ", s)
